package Day7Variables;

public class VariableExample1 {

	public static void main(String[] args) {
		System.out.println("program Starts");
		//syntax to declare a variable: datatype variablename;
		/* datatype------> tells us what type of value this variable is going to hold
		byte, short, int, long --> positive or negative number
		float, double          --> decimal numbers
		char                   --> character/alphabet 
		boolean                --> true/false
	*/		
		byte bytevariable;
		short shortvariable;
		int intvariable;
		long longvariable;	
		float floatvariable;
		double doublevariable;
		char charvariable;
		boolean booleanvariable;
		//Syntax to initlize a variable: variablename = value;
		bytevariable=25;
		shortvariable=30;
		intvariable=35;
		longvariable=40;
		floatvariable=12.6f;
		doublevariable=11.11;
		charvariable='A';
		booleanvariable=true;
		System.out.println("Program Ends");
	}
		

}
